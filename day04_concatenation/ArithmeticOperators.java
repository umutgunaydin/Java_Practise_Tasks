package day04_concatenation;

public class ArithmeticOperators {
    public static void main(String[] args) {

        System.out.println(112 + 354);

        System.out.println(10 / 3);// it gives 3 because of int
        System.out.println(10.0 / 3);// it gives 3.33....

        System.out.println(10d / 3);//by adding d we can make the result decimal

        System.out.println((int) (2.5 / 0.5)); // this usage makes it int

        System.out.println(10 % 3);//this is mod. it gives the remaining

        //remainder = numerator - (denominator * int result
        //20%6=20-(6*3)
    }
}
