package day04_concatenation;

public class Concatenation_vs_Addition {
    public static void main(String[] args) {

        System.out.println("32"+1);// will be 321

        System.out.println(7+"1");// will be 71

        System.out.println("13"+"2");// will be 132

        System.out.println("--------------------------");

        System.out.println(12 +2.5);// will be 14.5


    }
}
