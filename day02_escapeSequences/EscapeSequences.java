package day02_escapeSequences;

public class EscapeSequences {
    public static void main(String[] args) {
        System.out.println("Hello Guys!\nToday we have java class!"); //this sequence adds a newline
        System.out.println("----------------------------------------");
        System.out.println("\tJava programming language");
        System.out.println("\t\t\tJava programming language"); //this sequence makes indentation
        System.out.println("-----------------------------------------");
        System.out.println("\\"); // this is used for making one backslash
        System.out.println("-----------------------------------------");
        System.out.println("my favorite tv show is \"the walking dead!\""); //this is used to type double quote


    }
}
/*
    1. \n: newline
    2. \t: paragraph space
    3. \\: single back slash
    4. \": double quote
 */