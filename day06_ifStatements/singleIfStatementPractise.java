package day06_ifStatements;

public class singleIfStatementPractise {
    public static void main(String[] args) {

        int number = 100;

        if (number % 2 == 0) {
            System.out.println("Even Number");
        }
        if (number % 2 != 0) {
            System.out.println("Odd Number");
        }


    }
}
