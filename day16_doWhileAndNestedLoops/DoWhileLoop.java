package day16_doWhileAndNestedLoops;

public class DoWhileLoop {
    public static void main(String[] args) {

        boolean condition=false;

        while (condition){
            System.out.println("while loop");
        }

        System.out.println("-------------------------");

        do {
            System.out.println("Do while loop");
        }while (condition);

    }
}
